//Assigning value to variables
var age = 40;
if (age < 2) {
    console.log("\nIn this stage of life, You are a baby!\n");
}
else if (age >= 2 && age < 4) {
    console.log("\nIn this stage of life, You are a toddler!\n");
}
else if (age >= 4 && age < 13) {
    console.log("\nIn this stage of life, You are a kid!\n");
}
else if (age >= 13 && age < 20) {
    console.log("\nIn this stage of life, You are a teenager!\n");
}
else if (age >= 20 && age < 65) {
    console.log("\nIn this stage of life, You are an adult!\n");
}
else if (age >= 65) {
    console.log("\nIn this stage of life, You are an elder!\n");
}
